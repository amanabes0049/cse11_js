var grade='A';
switch(grade){
    case 'A':
        console.log("Excellent");
        break;
    case 'B':
        console.log("Very Good");
        break;
    case 'C':
        console.log("Average");
        break;
    case 'D':
        console.log("Below Average");
        break;
    case 'F':
        console.log("Failing");
        break;
    default:
        console.log("Unknown grade");
}